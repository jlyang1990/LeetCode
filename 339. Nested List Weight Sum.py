class Solution(object):
    def depthSum(self, nestedList):
        """
        :type nestedList: List[NestedInteger]
        :rtype: int
        """
        result = [0]
        self.depthSumHelper(nestedList, 1, result)
        return result[0]
        
    
    def depthSumHelper(self, nestedList, depth, result):
        for l in nestedList:
            if l.isInteger():
                result[0] += l.getInteger() * depth
            else:
                self.depthSumHelper(l.getList(), depth+1, result)